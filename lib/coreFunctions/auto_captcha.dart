    double totalMatch = 0.0;
    int? height = toMatch?.length;
    int width = toMatch![0].length;

    for (var x = 0; x < height!; x++) {
      for (var y = 0; y < width; y++) {
        if (toMatch[x][y] == 1.toString() && y + move < 150) {
          totalMatch += 1;
          int? offset = offSet[char];
          if (captcha[x + offset!][y + move] == 1) {
            properMatch += 1;
          }
        }
      }
    }
    if (totalMatch != 0 && properMatch / totalMatch > error) {
// console.log(char + ": " + properMatch/totalMatch)
      return {'char': char, 'width': width};
    }
  }
  return null;
}

getCaptcha(captcha) {
  var prevWidth = 0;
  var move = 0;
  var res = "";

  for (var i = 0; i < 6; i++) {
    double error = 1;
    Map? ch;
    while (error > 0.84 && ch == null) {
      for (var x = 0; x < 20; x++) {
        if (res.length == 6) {
          break;
        }
        ch = parseCharacter(captcha, move + x, error);
        if (ch != null) {
          prevWidth = ch["width"];
          res = res + ch["char"];
          move += prevWidth + x;
          break;
        }
      }
      error -= 0.05;
    }
  }

  return res;
}

